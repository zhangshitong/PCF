package com.iSupervision.domain;


public class UnitMap {

	private Long id;
	
    /** 单位名称 */
    private String unitName;

    public UnitMap(){}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getUnitName() {
		return unitName;
	}

	public void setUnitName(String unitName) {
		this.unitName = unitName;
	}
}
